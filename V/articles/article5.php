                font-weight: 200;">Ce matin du 26 novembre, en salle B2-20, les anciens sont réunis pour faire le bilan de cette première année de l’association et discuter des prochains évènements.</p>

            	<p style="text-align: justify;
                font-size: 17px;
                font-family: Roboto, sans-serif;
                line-height: 1.5;
                font-weight: 200;">Au programme de cette AG, les news du département Informatique et de l’association … et bien plus encore .</p>

