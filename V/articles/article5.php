				<h2>Rétrospective</h2>

				<p style="text-align: justify;
                font-size: 17px;
                font-family: Roboto, sans-serif;
                line-height: 1.5;
                font-weight: 200;">Ce fût une année riche pour l’association bien que peu de choses soient concrètement visibles pour les adhérents. Ce sont avant tout des tâches administratives telles que la création de l’association auprès de la préfecture, l’ouverture du compte en banque …</p>

				<p style="text-align: justify;
                font-size: 17px;
                font-family: Roboto, sans-serif;
                line-height: 1.5;
                font-weight: 200;">Ce fût aussi l’occasion pour nous de mettre en place le site web de l’association, celui qui vous permettra de nous retrouver mais aussi d’échanger et de partager tous ensemble.</p>

				<h2>Présentation des comptes</h2>

				<p style="text-align: justify;
                font-size: 17px;
                font-family: Roboto, sans-serif;
                line-height: 1.5;
                font-weight: 200;">Cette année, quelques dépenses et achats ont été faits: principalement, des cotisations et des achats pour la mise en place du site (plugins, serveur .. ). Les comptes ont été clôturés pour cette année avec un crédit de <strong>285,16</strong> euros.</p>

				<h2>Site web</h2>

				<p style="text-align: justify;
                font-size: 17px;
                font-family: Roboto, sans-serif;
                line-height: 1.5;
                font-weight: 200;">Grâce aux efforts conjugués de Julien Couturier, ainsi que d’une équipe de master menée par Thomas Yvard et Nabila Bensaber,&nbsp; l’association possède maintenant son site web.&nbsp;Vous pourrez y retrouver des articles sur les dernières actualités que ce soit sur l’association, le département informatique, ou des sujets techniques ou professionnels.</p>

				<p style="text-align: justify;
                font-size: 17px;
                font-family: Roboto, sans-serif;
                line-height: 1.5;
                font-weight: 200;">Une section “Offres d’emploi” a été créée afin que vous puissiez en déposer&nbsp;ou en consulter. Cela peut concerner aussi des stages ou des contrats d’apprentissage.</p>

