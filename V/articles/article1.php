						<h2>3/ Réflexion sur la mission de B2-20, animé par Lauris Braka</h2>

						<p>On recense trois principaux acteurs de l’association : les étudiants, les membres actifs et l’ensemble des diplômés. Nous relevons <b>deux freins </b>majeurs au déploiement de B2-20 :</p>

						<ul>
							<li>Les diplômés de l’IUT Descartes ont pour certains poursuivi leurs études au sein d’établissements qui disposent également d’un réseau d’alumni. Le risque est fort d’apporter via B2-20 <b>des services redondants </b>avec ceux des autres associations ;</li>
							<li>Le réseautage n’est <b>pas naturel </b>dans les métiers techniques.</li>
						</ul>

						<p>B2-20 doit donc apporter une réelle <b>valeur ajoutée </b>pour se distinguer et être attrayante.</p>

							</ul>

						<h3><b>3.1/ Tour de table : réponse à la question “pourquoi êtes-vous venus ce soir ?”</b></h3>
					<p>Les participants se sont exprimés tour à tour, voici une synthèse de leurs réponses :</p>
						<ul>
							<li>Pour transmettre aux étudiants :
							<ul>
							<li>Qu’est-ce que l’informatique réellement en entreprise ;</li>
							<li>Fournir des conseils pour la suite de leur vie professionnelle, témoigner ;</li>
							<li>Challenger leurs souhaits de carrière, les ramener “au monde réel” ;</li>
						</ul>
						</li>
							<li>Par nostalgie : pour revoir Hélène et Véronique, d’anciens profs, d’anciens camarades ;</li>
							<li>Pour aider au développement de l’asso, organiser des évènements ;</li>
							<li>Pour encourager les femmes à aller vers l’informatique ;</li>
							<li>Par curiosité : pour découvrir ce qu’est l’asso ;</li>
							<li>Pour étendre son réseau :
						<ul>
							<li>Échanger et apprendre des autres ;</li>
							<li>Rencontrer des anciens et s’ouvrir à de potentielles opportunités professionnelles ;</li>
							<li>Pour bénéficier d’une communauté (l’évolution dans certains grands groupes semble très déterministe et fortement corrélée aux diplômes).</li>
						</ul>
						</li>
						</ul>
						<h3><b>3.2/ Point de situation : le DUT et l’IUT Descartes</b></h3>
<p>Hélène Dirani a exposé la situation actuelle des DUT et en particulier celui de Descartes :</p>
<ul>
<li><b>Baisse de l’image </b>du diplôme Bac+2 dans les milieux professionnels au profit des Bac+3/5 ;</li>
<li>Étude du DUT en <b>180 ECTS </b>(3 ans), en réponse au souhait du ministère de favoriser les formations Bac+3 à l’université, nécessitant une réflexion sur le positionnement et la différenciation de la formation vis-à-vis des licences pro notamment ;</li>
<li><b>Fusion des Universités </b>Paris5 Descartes et Paris7 Diderot pour former l’Université de Paris : les IUT ont un statut particulier rapport aux composantes classiques des universités. La place de l’IUT Paris Descartes dans la nouvelle université de Paris n’est pas encore définie.</li>
</ul>
<p>Dans ce contexte très incertain, il semble indispensable de <b>promouvoir le diplôme</b>, et B2-20 pourrait tout à fait endosser ce rôle. D’autant plus que les inscriptions de jeunes bacheliers en DUT info ont grimpé de <b>15% </b>pour les IUT cette année via Parcoursup. Les chiffres sont stables pour les BTS. Cela vient confirmer la <b>légitimité </b>de notre diplôme.</p>
<p>En outre, au cours des échanges, les participants ont souligné les éléments suivants :</p>
<ul>
<li>Le DUT info Paris Descartes est <b>pertinent </b>par rapport aux autres formations de Bac+2 ;</li>
<li>Les titulaires d’un DUT ont un niveau de <b>qualification remarquable </b>et parfois méconnu ;</li>
<li>Nombre de diplômés ont poursuivi leurs études dans diverses directions. Le <b>large vivier d’anciens </b>permet de plus un retour d’expérience à tout âge. Cela apporte une grande diversité dans les parcours, compétences et visions. Ces richesses sont à promouvoir et constituent un levier pour attirer de nouveaux membres à s’investir.</li>
</ul>
<h3><b>3.3/ Synthèse : quelle doit être la mission de B2-20 ?</b></h3>
<p>En résumé, les idées phares des échanges sont les suivantes :</p>
<ul>
<li><b>Accompagner </b>les étudiants et jeunes diplômés dans le début de leur vie professionnelle ;</li>
<li><b>Faciliter </b>la recherche d’emploi, le recrutement et les opportunités commerciales ;</li>
<li><b>Favoriser </b>les échanges et conseils sur des sujets informatiques (et pas que) ;</li>
<li><b>Promouvoir </b>le DUT Informatique.</li>
</ul>
<h2><b>4/ Moyens</b></h2>
<h3><b>4.1/ Ressource indispensable : une communauté réactive</b></h3>
<p>Pour être en mesure de remplir ses missions, B2-20 devra pouvoir compter sur une communauté fidèle et réactive. Pour ce faire, il sera nécessaire de s’attarder plus précisément sur les <b>envies, attentes, contraintes des diplômés</b>, afin d’établir un plan de communication pertinent.</p>
<p>Ceci dit, quelques leviers de motivation ont d’ores et déjà été identifiés en séance :</p>
<ul>
<li>La nostalgie ;</li>
<li>L’envie de transmettre ;</li>
<li>La curiosité, l’envie de rencontrer de nouvelles têtes, l’envie d’apprendre ;</li>
<li>L’ennui au travail, le besoin de se réaliser par une activité associative annexe ;</li>
<li>Les opportunités de recrutement sur un marché très tendu ;</li>
<li>Les opportunités business ;</li>
<li>Et la défense du diplôme.</li>
</ul>
<h3><b>4.2/ Quels évènements ?</b></h3>
<p>Les évènements portés par B2-20 devront apporter une réelle valeur ajoutée. Voici quelques idées qui ont été proposées, qu’il s’agira de compléter et d’affiner par la suite :</p>
<ul>
<li>Mentorat ancien – étudiant et ancien – jeune diplômé ;</li>
<li>Conférences techniques, formations des étudiants, simulations d’entretiens et conseils CV ;</li>
<li>Newsletter ;</li>
<li>Logo, stickers ;</li>
<li>Compte LinkedIN pour le recrutement ;</li>
<li>Channel Bière sur Slack pour l’organisation d’apéros.</li>
</ul>
<h3><b>4.3/ Communication</b></h3>
<p>Un outil de communication <b>accessible à toutes les générations </b>de diplômés et les étudiants est indispensable. Les principaux usages ciblés sont l’animation de la vie de l’association, la communication sur les évènements, la recherche ou proposition d’emplois, la demande d’aide et de conseils techniques ou non techniques.</p>
<p>Le choix commun s’est porté vers <b>Slack</b>, qui a le mérite d’être facile de prise en main, de plus en plus populaire en entreprise, capable de proposer plusieurs espaces d’échanges dédiés et en outre déjà utilisé actuellement pour le fonctionnement de l’association. La décision a ainsi été prise d’<b>ouvrir le Slack </b>à l’ensemble des diplômés, sans modération.</p>
<p>Tous les diplômés sont ainsi conviés à s’inscrire sur le <a href="https://join.slack.com/t/b220/shared_invite/enQtNDQwNzUzMjc1NzAzLWU0NDE0OWU3MDFjMTU4NGRmYTJhMzBmYmVkMTNhZWVlNDg5MDFlODc1ZjIyYWIzZjI2NzVhYzhlNzdmMGExZDU">Slack</a> (lien d’invitation valable 1 mois seulement).</p>
<h2><b>5/ Célébrations des 50 ans de l’IUT</b></h2>
<p>Hélène Dirani et Véronique Bedos ont présenté le contexte des festivités qui auront lieu à l’IUT la soirée du <b>jeudi 10 janvier 2019</b>. Une équipe projet B2-20 va être créée pour organiser les activités portées par l’association. Deux idées ont suscité l’enthousiasme :</p>
<ul>
<li>Une salle par décennie de promos, des trombinoscopes et des objets de l’époque ;</li>
<li>Un ancien cours en amphithéâtre animé par le professeur de l’époque (DG ?).</li>
</ul>
<p>En revanche, le département info et B2-20 n’ont <b>pas ou très peu de budget </b>dédié aux 50 ans. Il est donc nécessaire de trouver des <b>subventions</b>. L’équipe projet organisera une réunion rapidement.</p>
<h2><b>6/ Partenariats</b></h2>
<p>Yannis Belkebir-Le Guen (L3 Miage) et Marcellino Dour (DUT 2 + BDE Info 2018-2019) portent un projet de création de <b>Junior-Entreprise</b> (JE) pour le département info. Ils sont parrainés par une autre JE mature, accompagnés par la Confédération Nationale des Junior-Entreprises (CNJE) et ont le support du Directeur de l’IUT Xavier Sense. Pour plus d’informations, vous pouvez vous reporter au Compte-Rendu de l’Assemblée Générale de B2-20 du 18 mai 2018. Merci de ne pas communiquer plus largement sur ce sujet pour le moment, qui reste au stade d’étude.</p>
<p>Le <b>BDE Général </b>est également très actif et sera un soutien de poids pour l’organisation des 50 ans de l’IUT, tel qu’il a pu l’être il y a 10 ans pour les 40 ans de l’IUT.</p>
<p>Il semble intéressant de soutenir ces deux associations, voire d’envisager un partenariat.</p>
