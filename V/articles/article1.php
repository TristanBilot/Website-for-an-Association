						<h2>1/ Introduction par le président sortant Gautier Laroche</h2>

						<ul>
							<li>B2-20 a été créée en <b>2015 </b>;</li>
							<li>Le site internet <a href="https://b220.fr">B220.fr</a> repose sur un wordpress avancé, permettant entre autres les fonctions suivantes : inscription, adhésion et gestion de l’adhésion, paiement CB, dons, écriture et publication d’articles, gestion complète d’évènements, annuaire et offres d’emploi.</li>
							<li>L’association a organisé des évènements depuis :
							<ul>
							<li><b>Journée Métiers </b>à l’IUT chaque année. La dernière, le 18 mai 2018, a rassemblé <b>17 anciens </b>qui ont témoigné de leur parcours auprès des étudiants de 1ère année ;</li>
							<li>Une AG pour lancer l’organisation des 50 ans de l’IUT le 18 mai, en présence de DG ;</li>
							<li>Journées thématiques : conférences sur la <a href="https://b220.fr/2017/04/21/journee-iot-a-liut/"><b>sécurité des objets connectés</b></a> en 2017, l’<b>Intelligence Artificielle</b> en 2018 et la <b>Blockchain</b> à venir en 2019 ;</li>
							<li>Formations thématiques aux étudiants : <b>formation Git </b>;</li>
							<li>Deux <i>game jams</i> et un apéro pétanque.</li>
							</ul>
							</li>
							<li>L’association a par ailleurs parrainé des évènements :
							<ul>
							<li>La <b>nuit de l’info</b> chaque année (<a href="https://b220.fr/2018/07/27/nuit-de-linfo-2017/">article 2017</a>) ;</li>
							<li>Encadrement de projets de S4 (anciennement PPD) ;</li>
							<li>Rencontres “filles” entre d’anciennes et actuelles étudiantes. Toujours intéressant, cela a parfois débouché sur des stages.</li>
							</ul>
							</li>
						</ul>

						<p>Hélène Dirani et Véronique Bedos ont souligné que B2-20 était <b>systématiquement mise en avant </b>auprès des étudiants. B2-20 est donc connue, et il semble intéressant de surfer sur cette vague.</p>

