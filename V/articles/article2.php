				<h1 class="titreArticle2" style="font-size: 40px;"itemprop="headline">Nuit de l’Info 2017</h1>
				<time class="entry-date published" datetime="2018-07-27T14:45:58+00:00" itemprop="datePublished">27 juillet 2018</time>
				<span class="author vcard" itemtype="https://schema.org/Person" itemscope="itemscope" itemprop="author">de <span class="author-name" itemprop="name">Hélène Dirani</span></a></span></p>


				<div class="introArticle2">
				<div class="imageNuitInfo"><img src="./Content/img/img-articles/article2.jpg" alt="" width="270%" height="35%">
				</div>

				<div class="article2texte1"><p>Chaque année en décembre, la Nuit de l’Info met en concurrence des équipes issues de différentes écoles et universités, pour un défi à relever en temps limité : du coucher du soleil au lever du soleil le lendemain matin.Depuis 5 ans, l’IUT Paris Descartes participe à cet évènement : les équipes sont majoritairement constituées d’étudiants de DUT Informatique, avec les renfort d’étudiants d’Info-Com (Information-Communication) et STID (Statistiques et Informatique Décisionnelle).</p><p>L’édition 2017 a connu un grand succès, avec une centaine de participants, répartis dans 6 équipes, sur le site de l’IUT.</p> 
				</div></div>

				<p class="article2texte2">Les anciens de la B2-20 ont répondu présents au travers des entreprises partenaires de l’évènement, (Coddity, LCL, Outscale, et SOAT), mais aussi par l’organisation d’ateliers techniques en amont de la Nuit de l’Info.</p>

				<p>Le sujet 2017 portait sur le thème de la sécurité routière <a href="http://www.nuitdelinfo.com/nuitinfo/start">http://www.nuitdelinfo.com/nuitinfo/start</a></p>

				<p>Dès le top départ, donné en amphi, les équipes se sont isolées dans leurs salles respectives, pour un premier débriefing du sujet, avant de choisir les défis à relever. Vers 20h, pause “pizzas” en salle B2-20 !</p>
				
				<div class="images3"><center><img src="https://i1.wp.com/b220.fr/wp-content/uploads/2018/07/IMG-3143.jpg?w=156&amp;h=207&amp;ssl=1" width="156" height="207" style="width: 20%; height: 23%;">
